#int는 숫자
stock_price = 5000
print(stock_price)
print(type(stock_price))

#float: 소수점을 가진 숫자!
stock_earning = 3.7
print(stock_earning)
print(type(stock_earning))

#string은 문자열
stock_name = "나는 문자입니다."
print(stock_name)
print(type(stock_name))

#boolean은 True or False만 존재
first_bool = True
second_bool = False
print(first_bool)
print(type(first_bool))
print(second_bool)
print(type(second_bool))
